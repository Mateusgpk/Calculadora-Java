public class Calculadora {
    public float somar (float a, float b){
        return  a+b;
    }

    public float subtrai (float a, float b){
        return  a-b;
    }
    public float multi (float a, float b){
        return  a*b;
    }
    public float div (float a, float b){
        return  a/b;
    }
}
